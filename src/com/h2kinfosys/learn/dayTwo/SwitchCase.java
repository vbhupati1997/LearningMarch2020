package com.h2kinfosys.learn.dayTwo;

public class SwitchCase {

	public static void main(String[] args) {
		int dayOfWeek = 10;
		
		switch(dayOfWeek) {
		case 0: {
			System.out.println("day of week is 0");
			break;			
		}
		case 1: {
			System.out.println("day of week is 1");
			break;			
		}
		case 2: {
			System.out.println("day of week is 2");
			break;			
		}
		case 3: {
			System.out.println("day of week is 3");
			break;			
		}
		case 4: {
			System.out.println("day of week is 4");
			break;			
		}
		case 5: {
			System.out.println("day of week is 5");
			break;			
		}
		case 6: {
			System.out.println("day of week is 6");
			break;			
		}
		case 7: {
			System.out.println("day of week is 7");
			break;			
		}
		
		default: {
			System.out.println("default");
		}
		}			
		
	}

}
